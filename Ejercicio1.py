numero1 = float( input("Introduce el 1er. número: ") )
numero2 = float( input("Introduce el 2do. número: ") )

print("Los números son iguales? ", numero1 == numero2)
print("Los números son diferentes?", numero1 != numero2)
print("El 1ro. es mayor que el 2do.?", numero1 > numero2)
print("El 2do. es mayor o igual que el 1ro.?", numero1 <= numero2)
